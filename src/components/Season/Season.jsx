export default function Season({season}) {
    switch(season) {
        case 'fall':
          return <p>Temporada de Outono</p>
          
        case "winter":
           return <p>Temporada de Inverno</p>
            
        case "spring":
           return <p>Temporada de Inverno</p>
            
        case "summer":
           return <p>Temporada de Inverno</p>
    }
}